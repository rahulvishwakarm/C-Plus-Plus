#include<iostream>
#include<conio.h>
#include<math.h>
using namespace std;

int main()
{
    float x = 9.2;
    cout<<"Value of x: "<<x<<endl;
    cout<<"Ceil value of x: "<<ceil(x)<<endl;
    cout<<"Floor value of x: "<<floor(x)<<endl;
    cout<<"Round value of x: "<<round(x)<<endl;
    cout<<"Trunc value of x: "<<trunc(x)<<endl;
}